function closeModalBtn() {
  const overlay = document.getElementById("projectModal");
  overlay.classList.remove("open");

  // Restore scrolling
  document.body.classList.remove("modal-open");
  document.documentElement.style.removeProperty("--modal-sb-pad");

  // Clear modal content after transition
  setTimeout(function () {
    const modalEl = document.getElementById("modalContent");
    if (modalEl) modalEl.scrollTop = 0;
  }, 300);
}

/* ══════════════════════════════
   CUSTOM CURSOR
══════════════════════════════ */
const cursor = document.getElementById("cursor");
const cursorRing = document.getElementById("cursorRing");
let mx = -200,
  my = -200,
  rx = -200,
  ry = -200;
let cursorVisible = false;

document.addEventListener("mousemove", (e) => {
  mx = e.clientX;
  my = e.clientY;
  if (!cursorVisible) {
    cursorVisible = true;
    rx = mx;
    ry = my;
    cursor.style.opacity = "1";
    cursorRing.style.opacity = "1";
  }
});

function animateCursor() {
  // Don't animate if CSS has hidden the cursor via media queries (touch devices)
  if (window.getComputedStyle(cursor).display === "none") {
    requestAnimationFrame(animateCursor);
    return;
  }

  cursor.style.left = mx + "px";
  cursor.style.top = my + "px";
  rx += (mx - rx) * 0.12;
  ry += (my - ry) * 0.12;
  cursorRing.style.left = rx + "px";
  cursorRing.style.top = ry + "px";
  requestAnimationFrame(animateCursor);
}
animateCursor();

function attachCursorHover() {
  document
    .querySelectorAll("a, button, .project-card, [onclick]")
    .forEach((el) => {
      if (el.dataset.ch) return; // already bound
      el.dataset.ch = "1";
      el.addEventListener("mouseenter", () => {
        cursor.classList.add("hover");
        cursorRing.classList.add("hover");
      });
      el.addEventListener("mouseleave", () => {
        cursor.classList.remove("hover");
        cursorRing.classList.remove("hover");
      });
    });
}

