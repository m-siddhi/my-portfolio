/* ══════════════════════════════
   MOBILE SWIPE FOR CAROUSEL
══════════════════════════════ */
function initCarouselTouch() {
  Object.keys(carouselState).forEach((id) => {
    const track = document.getElementById("carousel-" + id);
    if (!track) return;

    let startX = 0;
    let currentX = 0;
    let isDragging = false;

    track.addEventListener(
      "touchstart",
      (e) => {
        startX = e.touches[0].clientX;
        isDragging = true;
      },
      { passive: true },
    );

    track.addEventListener(
      "touchmove",
      (e) => {
        if (!isDragging) return;
        currentX = e.touches[0].clientX;
      },
      { passive: true },
    );

    track.addEventListener("touchend", () => {
      if (!isDragging) return;
      isDragging = false;

      const diff = startX - currentX;
      // If user swiped left (next)
      if (diff > 50) slideCarousel(id, 1);
      // If user swiped right (previous)
      else if (diff < -50) slideCarousel(id, -1);
    });
  });
}

