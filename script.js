/* ══════════════════════════════
   CAROUSEL
══════════════════════════════ */
const carouselState = { frontend: 0, uiux: 0, uxcase: 0 };

function slideCarousel(id, dir) {
  const track = document.getElementById("carousel-" + id);
  const cards = track.querySelectorAll(".project-card");
  if (!cards.length) return;
  const wrapperW = track.parentElement.offsetWidth;
  // Read actual gap from computed style so mobile (gap:0) and desktop (gap:28px) both work
  const computedGap = parseFloat(getComputedStyle(track).gap) || 0;
  const cardW = cards[0].offsetWidth + computedGap;
  const visible = Math.max(1, Math.round(wrapperW / cardW));
  const max = Math.max(0, cards.length - visible);
  carouselState[id] = Math.max(0, Math.min(max, carouselState[id] + dir));
  track.style.transform = "translateX(-" + carouselState[id] * cardW + "px)";
  document.getElementById("prev-" + id).disabled = carouselState[id] === 0;
  document.getElementById("next-" + id).disabled = carouselState[id] >= max;
}

function initCarouselBtns() {
  Object.keys(carouselState).forEach((id) => {
    const prev = document.getElementById("prev-" + id);
    if (prev) prev.disabled = true;
  });
}

