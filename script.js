let currentPage = "home";

function navigate(page) {
  const target = document.getElementById("page-" + page);
  if (!target) return; // guard FIRST — don't touch DOM if page doesn't exist
  document
    .querySelectorAll(".page")
    .forEach((p) => p.classList.remove("active"));
  target.classList.add("active");
  currentPage = page;
  window.scrollTo(0, 0);
  updateNavTheme(page);
  setTimeout(triggerReveals, 80);
  setActiveNavLink(page);
}

function navTo(sectionId) {
  if (currentPage !== "home") {
    navigate("home");
    setTimeout(() => smoothScrollTo(sectionId), 120);
  } else {
    smoothScrollTo(sectionId);
  }
  closeMobileMenu();
}

function smoothScrollTo(sectionId) {
  const el = document.getElementById(sectionId);
  if (!el) return;
  const navH = document.getElementById("main-nav").offsetHeight;
  const top = el.getBoundingClientRect().top + window.scrollY - navH - 8;
  window.scrollTo({ top, behavior: "smooth" });
}

function updateNavTheme(page) {
  const nav = document.getElementById("main-nav");
  if (page === "home") {
    syncNavToScroll();
  } else {
    nav.classList.add("dark");
  }
}

function syncNavToScroll() {
  const nav = document.getElementById("main-nav");
  const hero = document.querySelector(".hero");
  if (!hero) return;
  const heroBottom = hero.offsetTop + hero.offsetHeight;
  nav.classList.toggle("dark", window.scrollY < heroBottom - 80);
}

function setActiveNavLink(page) {
  document
    .querySelectorAll(".nav-links a")
    .forEach((a) => a.classList.remove("active"));
  if (page === "projects" || page === "case-study") {
    const workLink = document.getElementById("nav-work");
    if (workLink) workLink.classList.add("active");
  }
}

function setupScrollSpy() {
  const navMap = {
    work: "nav-work",
    skills: "nav-skills",
    "know-me": "nav-about",
    contact: "nav-contact",
  };
  const io = new IntersectionObserver(
    (entries) => {
      if (currentPage !== "home") return;
      entries.forEach((entry) => {
        const navEl = document.getElementById(navMap[entry.target.id]);
        if (!navEl || !entry.isIntersecting) return;
        document
          .querySelectorAll(".nav-links a")
          .forEach((a) => a.classList.remove("active"));
        navEl.classList.add("active");
      });
    },
    { threshold: 0.3, rootMargin: "-60px 0px -60px 0px" },
  );
  Object.keys(navMap).forEach((id) => {
    const el = document.getElementById(id);
    if (el) io.observe(el);
  });
}

